Maze::Maze(const std::string& mazeID) {
    loadMaze(mazeID);
    rows = grid.size();
    cols = grid.empty() ? 0 : grid[0].size();
}

void Maze::loadMaze(const std::string& mazeID) {//add more maze IDs here as you impliment more mazes
    if (mazeID == "maze1") {
        grid = maze1Data;
    }
    else if (mazeID == "maze2") {
        grid = maze2Data;
    }
    else {
        throw std::invalid_argument("Unknown maze ID: " + mazeID);
    }
}

bool Maze::isWalkable(int x, int y) const {
    return x >= 0 && x < rows && y >= 0 && y < cols &&
        (grid[x][y] == 0 || grid[x][y] == 3);
}

 

void Maze::printMaze() const {
     for (const auto& row : grid) {
         for (int cell : row) {
             switch (cell) {
             case 0: std::cout << "  "; break;
             case 1: std::cout << "##"; break;
             case 2: std::cout << "S "; break;
             case 3: std::cout << "E "; break;
             case 9: std::cout << "R "; break;
             default: std::cout << "??"; break;
             }
         }
         std::cout << '\n';
     }
}



int Maze::getRows() const { return rows; }
int Maze::getCols() const { return cols; }
int Maze::getTile(int x, int y) const {
    return grid.at(x).at(y); // bounds-safe
}

void Maze::setTile(int x, int y, int value) {
    grid.at(x).at(y) = value;
}